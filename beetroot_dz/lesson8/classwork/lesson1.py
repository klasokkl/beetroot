# Створіть таблицю множення на конкретне число.
# 1 * 1 = 1
# 2 * 1 = 2
# 3 * 1 = 3
# 4 * 1 = 4
# .........

# 1. Формування таблиці реалізовано як функція
# 2. Функція приймає параметер на яке число формується таблиця множення
# 3. Як результат в консоль виводиться шаблон показаний вище

def table(namber):
    for i in range(1, 11):
        print(f'{i} * {namber} = {i * namber}')

table(2)